	public function Find($freq, $error = 3000000)
	{
		if($freq <= 0) return 0;

		$mindiff = -1;
		$mindiffchannel = 0;

		foreach($this->freqs as $channel => $freq2)
		{
			$diff = abs($freq2 - $freq);

			if($diff <= $error && (!$mindiffchannel || $diff < $mindiff))
			{
				$mindiff = $diff;
				$mindiffchannel = $channel;
			}

			if($mindiff == 0 || $mindiffchannel && $mindiff < $diff)
			{
				break;
			}
		}

		return $mindiffchannel;
	}
